import Head from "next/head";
import Layout from "../components/Layout";
import Hero from "../components/Hero";
import Services from "../components/Services";
import Menu from "../components/Menu";
import css from "../styles/Home.module.css";
// import css from "../styles/Login.module.css";
import { client } from "../lib/client";
import { useState } from "react";

export default function Home({ biryanis }) {
  const [isLoggedIn, setisLoggedIn] = useState(false);
  const [username, setUsername] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");

  const handleLogin = () => {
    if (username === "admin" && password === "admin") {
      setisLoggedIn(true);
    } else {
      setError("Invalid username or password");
    }
  };

  return isLoggedIn ? (
    <Layout>
      <div className={css.containeer}>
        <Head>
          <title>BiryaniHub</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/Logo.png" />
        </Head>
        {/* body */}
        <main>
          <Hero />
          <Services />
          <Menu biryanis={biryanis} />
        </main>
      </div>
    </Layout>
  ) : (
    <div className={css.container}>
      <div className={css.wrapper}>
        <h1>Login</h1>
        <input
          type="text"
          placeholder="Username"
          className={css.input}
          value={username}
          onChange={(e) => setUsername(e.target.value)}
        />
        <br />
        <input
          type="password"
          placeholder="Password"
          className={css.input}
          value={password}
          onChange={(e) => setPassword(e.target.value)}
        />
        <br />
        <button className={css.button} onClick={handleLogin}>
          Login
        </button>
        {error && <p className={css.error}>{error}</p>}
      </div>
    </div>
  );
}

export const getServerSideProps = async () => {
  const query = '*[_type == "biryani"]';
  const biryanis = await client.fetch(query);
  return {
    props: {
      biryanis,
    },
  };
};
